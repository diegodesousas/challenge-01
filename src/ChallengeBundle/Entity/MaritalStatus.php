<?php

namespace ChallengeBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * MaritalStatus
 *
 * @ORM\Table(name="marital_status")
 * @ORM\Entity(repositoryClass="ChallengeBundle\Repository\MaritalStatusRepository")
 */
class MaritalStatus
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="description", type="string", length=255, unique=true)
     */
    private $description;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set description
     *
     * @param string $description
     *
     * @return MaritalStatus
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }
}
